#include "../include/libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*temp;

	if (!lst)
		return (0);
	temp = lst;
	i = 0;
	while (lst)
	{
		lst = lst->next;
		i++;
	}
	lst = temp;
	return (i);
}
