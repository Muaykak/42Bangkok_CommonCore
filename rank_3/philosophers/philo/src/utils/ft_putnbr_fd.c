#include "philo.h"

void	ft_putnbr_fd(long long n, int fd)
{
	char	c;

	if (fd < 0)
		return ;
	if (n < -9223372036854775807)
	{
		write(fd, "-9223372036854775807", 20);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = n * -1;
	}
	if (n / 10 != 0)
	{
		ft_putnbr_fd((n / 10), fd);
	}
	c = (n % 10) + 48;
	write(fd, &c, 1);
}
